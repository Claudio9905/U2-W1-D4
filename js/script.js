firstPost.addEventListener("mouseenter", () => {
  const pin = document.getElementById("punteruolo1");
  pin.classList.add("punteruoloUp");
});

firstPost.addEventListener("mouseleave", () => {
  const pin = document.getElementById("punteruolo1");
  pin.classList.remove("punteruoloUp");
});

